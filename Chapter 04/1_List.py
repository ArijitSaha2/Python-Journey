# Lists are containers to store any data type.

friends = ["Apple","Orange",337,10.00,"Akansha","Jacky",90] # List syntax = []
print(friends[0])

friends[0] = "Melon" # Unlike Strings Lists are Mutable. (Inshort they can be changed)

print(friends[0])
print(friends[1:4]) # As we Did in Strings the last index gets ignored


a = ['Kiwi','Papaya',4000,100.21,'Grace','Rosie']
