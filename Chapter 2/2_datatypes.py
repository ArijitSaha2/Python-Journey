# DATA TYPES =  data type is used to classify what kind of value a variable can hold. 
# For example, int can hold whole numbers, float for numbers with decimal points, string for text, boolean for True or False values, and None represents the absence of a value.

# Primarily these are the following data types in Python:   

# 1. Integer
# 2. Floating point numbers 
# 3. Strings 
# 4. Booleans 
# 5. None 
# Python is a fantastic language that automatically identifies the type of data for us. 







a = 10       # a is an Integer

b = 10.10    # b is a floating point number

c = "Harry"  # c is a string

d = False    # d is a Boolean variable

e = None     # e is a none type variable

