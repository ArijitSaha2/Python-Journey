# Q: What is the difference between a variable and a keyword in Python?

"""A variable is a name used to store data (like a container for values), and its contents can change during program execution.
A keyword is a reserved word in Python that has a predefined meaning and purpose in the language (like if, else, while, def). You cannot use keywords as variable names. """

