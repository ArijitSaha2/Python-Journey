# WHILE LOOP:
# In while loops, the condition is checked first.
# If the condition is true, the loop body executes.
# The process of checking the condition and executing the body repeats until the condition is false
i = 1
while(i<6):# The block keeps executing until the condition is true
    print(i)
    i += 1

"""
Output:
1
2
3
4
5
"""