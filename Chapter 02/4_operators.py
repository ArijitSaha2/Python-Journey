# OPERATORS IN PYTHON 

# Following are some common operators in python: 

# 1. Arithmetic operators: + , - , * , etc. 

# 2. Assignment operators: = , += , -= , etc 

# 3. Comparison operators:== ,> ,>= ,< ,! , etc

# 4. Logical operators: and , or , not 


# Arithmetic Operators
A = 10
B = 30
C = A-B # '-' is an Arithmetic Operator
print(C)


# Assignment Operator
a = 7-1 # Evaluates and assigns the Value to a
print(a)

b  = 6 # Assign 6 in b
b += 20 # Increment the Value of b by 20 and assign it to b
b -= 1  # Decrement the value of b by 1 and assign it to b
b *= 10 # Multiplies the value of b by 1 and assign it to b
print(b)


# Comparison Operator
d = 100
e = 20
print(e!=d)
